import UIKit

extension UIColor {
    static var keyBlackPressed:UIColor {
        return #colorLiteral(red: 0.4078193307, green: 0.4078193307, blue: 0.4078193307, alpha: 1)
    }
    
    static var keyBlack:UIColor {
        return .black
    }
    
    static var keyWhitePressed:UIColor {
        return #colorLiteral(red: 0.6000000238, green: 0.6000000238, blue: 0.6000000238, alpha: 1)
    }
    
    static var keyWhite:UIColor {
        return .white
    }
}
